  <h4 class="mb-4 text-justify">
    Sistem Pakar Penentuan Jenis Pelatihan Kerja adalah aplikasi cerdas yang dirancang untuk membantu
    pencari kerja menemukan program pelatihan yang paling sesuai dengan profil mereka.  
    Dengan memasukkan data seperti usia, pendidikan, jenis kelamin, dan minat,  
    sistem ini menggunakan metode <em>forward chaining</em> untuk menelusuri aturan-aturan  
    dan menghasilkan rekomendasi pelatihan secara otomatis.
  </h4>

  <h4 class="mb-4 text-justify">
    <strong>Menganalisis Profil dan Minat</strong><br>
    Sistem ini memetakan kombinasi data minat (usia, jenis kelamin, pendidikan, minat) ke 
    kumpulan rule yang telah ditetapkan. Hasilnya adalah rekomendasi pelatihan yang  
    sangat terpersonalisasi—mulai dari Web Developer hingga Barista—sesuai kebutuhan pengguna.
  </h4>

  <h4 class="mb-4 text-justify">
    <strong>Data Pelatihan Terkini</strong><br>
    Kami rutin memperbarui basis data pelatihan dengan materi dan kurikulum terbaru  
    dari berbagai institusi kerja sama. Hal ini menjamin bahwa setiap rekomendasi  
    merefleksikan standar industri dan tren pasar kerja terkini.
  </h4>

  <h4 class="mb-4 text-justify">
    <strong>Keamanan dan Privasi Data</strong><br>
    Privasi data pengguna adalah prioritas. Seluruh informasi pribadi dan input data minat  
    disimpan dengan enkripsi, dan hanya digunakan untuk proses inferensi sistem,  
    tanpa dibagikan ke pihak lain.
  </h4>
